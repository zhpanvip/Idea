public class AdvicePost {
    private String phone;
    private String suggestion;
    private String user_type;
    private String user_id;
    private String interface_source;

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public String getSuggestion() {
        return suggestion;
    }

    public void setSuggestion(String suggestion) {
        this.suggestion = suggestion;
    }

    public String getUser_type() {
        return user_type;
    }

    public void setUser_type(String user_type) {
        this.user_type = user_type;
    }

    public String getUser_id() {
        return user_id;
    }

    public void setUser_id(String user_id) {
        this.user_id = user_id;
    }

    public String getInterface_source() {
        return interface_source;
    }

    public void setInterface_source(String interface_source) {
        this.interface_source = interface_source;
    }
}
